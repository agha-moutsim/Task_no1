

//CALCULATER#

var calculater = prompt("Enter your First Number");
var calculater1 = prompt("Enter your Second Number");
var operater = prompt("Enter your oprater");

if(operater ==="+"){
    console.log(calculater + calculater1);
}

else if(operater === "-"){
    console,log(calculater - calculater1);
}

else if(operater === "*"){
    console,log(calculater * calculater1);
}

else if(operater === "%"){
    console.log(calculater % calculater1);
}

else if(operater === "/"){
    console.log(calculater / calculater1);
}






